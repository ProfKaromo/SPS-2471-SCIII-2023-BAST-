#Loops
Age = [21,19,23,22,25]
for i in Age:
    print(i)

var = 'Apple'
for i in var:
    print(i,end=' ')

Age = [21,19,23,22,25]
print('\n')
for index,i in enumerate(Age):
    print('Index'+str(index)+":"+str(i))

# While Loop
x = 6
while x <= 10:
    print(x)
    x += 1
for i in range(2,10,2):
    print(i)

