# Control Structures
# If...else statement
years = 20
if years>=18:
    print('You are an adult.')
    print('Yes you are')
else:
    print('You are a child.')

n = int(input('Enter a number:'))
if n>0:
    print(n,' is a positive number.\n')
    print(n*2)
elif n==0:
    print(n,' is a zero.\n')
    print('Nothing much')
else:
    print(n,' Is a negative number.\n')
    print(n/n)

